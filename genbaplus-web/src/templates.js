// メッセージテンプレート

function getSeasonalGreeting() {
  const month = new Date().getMonth() + 1;
  const greetings = {
    1:  'お世話になっております。新年もどうぞよろしくお願いいたします。',
    2:  'お世話になっております。寒い日が続きますが、いかがお過ごしでしょうか。',
    3:  'お世話になっております。春の気配を感じる季節になってまいりました。',
    4:  'お世話になっております。桜の季節となりました。',
    5:  'お世話になっております。爽やかな季節となりました。',
    6:  'お世話になっております。梅雨の時期となりました。',
    7:  'お世話になっております。暑い日が続いておりますが、いかがお過ごしでしょうか。',
    8:  'お世話になっております。暑さが厳しい時期となりましたが、体調にはお気をつけください。',
    9:  'お世話になっております。秋の気配を感じる季節になってまいりました。',
    10: 'お世話になっております。秋も深まってまいりました。',
    11: 'お世話になっております。朝夕が涼しくなってまいりました。',
    12: 'お世話になっております。年の瀬が近づいてまいりました。'
  };
  return greetings[month] || 'お世話になっております。';
}

function generateWeeklyMessage() {
  const greeting = getSeasonalGreeting();
  return `${greeting}

現場の皆様、いつもお疲れ様です。

【写真格納・進捗報告のお願い】

今週の現場写真を、期日までに現場Plusへ格納いただきますようお願いいたします。

■ 格納対象
・工程写真（作業前・中・後）
・安全管理写真
・品質管理写真

■ 格納期限
今週金曜日 17:00まで

写真の格納が完了しましたら、進捗報告も合わせてご入力をお願いいたします。

不明点がございましたら、担当者までご連絡ください。

引き続き、安全第一でよろしくお願いいたします。`;
}

function generateMonthlyMessage() {
  const greeting = getSeasonalGreeting();
  const now = new Date();
  const month = now.getMonth() + 1;
  const year = now.getFullYear();

  return `${greeting}

現場の皆様、今月もお疲れ様でした。

【${year}年${month}月分 出荷証明書の格納について】

今月分の出荷証明書を、現場Plusへご格納いただきますようお願いいたします。

■ 格納対象
・コンクリート出荷証明書
・鉄筋出荷証明書
・その他材料の品質証明書

■ 格納期限
今月末まで

証明書の格納が完了しましたら、ご連絡いただけますと幸いです。
漏れがないようご確認のほど、よろしくお願いいたします。

ご不明な点がございましたら、担当者までお気軽にお問い合わせください。

今月もお疲れ様でございました。`;
}

module.exports = { generateWeeklyMessage, generateMonthlyMessage };
